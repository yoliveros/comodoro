#pragma once

#include <stdint.h>

// Signed int
typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

// Unsigned int
typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

// Floating point
typedef float f32;
typedef double f64;

// Boolean
typedef i8 b8;
typedef i32 b32;

#define TRUE 1;
#define FALSE 0;
